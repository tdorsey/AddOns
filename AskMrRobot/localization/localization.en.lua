function AskMrRobot.format(fmt, ...)
    local args, order = {...}, {}

    local f = fmt:gsub('%%(%d+)%$', function(i)
        tinsert(order, args[tonumber(i)])
        return '%'
    end)

    return f:format(unpack(order))
end

